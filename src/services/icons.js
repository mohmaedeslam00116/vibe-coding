export function getFileIcon(filePath) {
  const ext = filePath.split('.').pop()?.toLowerCase()
  const name = filePath.split('/').pop()?.toLowerCase()

  const iconMap = {
    'html': icons.html,
    'htm': icons.html,
    'css': icons.css,
    'scss': icons.css,
    'sass': icons.css,
    'less': icons.css,
    'js': icons.js,
    'mjs': icons.js,
    'jsx': icons.js,
    'ts': icons.code,
    'tsx': icons.code,
    'json': icons.json,
    'md': icons.markdown,
    'mdx': icons.markdown,
    'svg': icons.image,
    'png': icons.image,
    'jpg': icons.image,
    'jpeg': icons.image,
    'gif': icons.image,
    'ico': icons.image,
    'yaml': icons.yaml,
    'yml': icons.yaml,
    'toml': icons.yaml,
    'sh': icons.terminal,
    'bash': icons.terminal,
    'zsh': icons.terminal,
    'env': icons.settings,
    'gitignore': icons.settings,
    'gitattributes': icons.settings,
  }

  if (name === 'package.json') return icons.json
  if (name === 'tsconfig.json') return icons.json
  if (name === 'vite.config.js') return icons.settings
  if (name === 'vite.config.ts') return icons.settings
  if (name === '.env') return icons.settings
  if (name === 'Dockerfile') return icons.terminal
  if (name === 'Makefile') return icons.terminal

  return iconMap[ext] || icons.file
}
